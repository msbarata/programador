    private void jButtonRaizActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonRaizActionPerformed
        try {
        double valor = Double.parseDouble(jTextFieldResultado.getText());
        if (valor < 0) {
            jTextFieldResultado.setText("Erro: Raiz negativa");
            return;
        }
        double resultado = Math.sqrt(valor);
        
        String resultadoEmTexto = String.format("%.0f", resultado);
        jTextFieldResultado.setText(resultadoEmTexto);
               
        limpar = true;
    } catch (NumberFormatException e) {
        jTextFieldResultado.setText("Erro");
    } 
    }//GEN-LAST:event_jButtonRaizActionPerformed

