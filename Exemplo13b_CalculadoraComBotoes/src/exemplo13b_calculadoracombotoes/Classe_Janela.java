    private void jButtonIgualActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonIgualActionPerformed
                
   sop2 = jTextFieldResultado.getText();
    
    try {
        double op1 = Double.parseDouble(sop1);
        double op2 = Double.parseDouble(sop2);
        double resultado = 0;
        
        switch (operador) {
            case "+":
                resultado = op1 + op2;
                break;
            case "-":
                resultado = op1 - op2;
                break;
            case "*":
                resultado = op1 * op2;
                break;
            case "/":
                if (op2 == 0) {
                    jTextFieldResultado.setText("Erro: Divisão por zero");
                    return;
                }
                resultado = op1 / op2;
                break;
            case "%":
               if (op1 == 0)  {
                    jTextFieldResultado.setText("Erro: Porcentagem por zero");
                    return;
                }         
                resultado = (op1 * op2) / 100;
                break;
                             
        }                                      
        String resultadoEmTexto = String.format("%.0f", resultado);                  
        jTextFieldResultado.setText(resultadoEmTexto);
        limpar = true;
        
    } catch (NumberFormatException e) {
        jTextFieldResultado.setText("Erro: Número inválido");
   }      
    }//GEN-LAST:event_jButtonIgualActionPerformed

